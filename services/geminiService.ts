let lastIndex = -1;

export function generateAsciiHeart(): string {
  let randomIndex;
  // Ensure we don't show the same heart twice in a row
  do {
    randomIndex = Math.floor(Math.random() * asciiHearts.length);
  } while (asciiHearts.length > 1 && randomIndex === lastIndex);

  lastIndex = randomIndex;
  return asciiHearts[randomIndex].trim();
}